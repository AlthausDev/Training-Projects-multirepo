    private void btnEnviar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviar2ActionPerformed
       try {
            // Obtener el destino seleccionado
            String destino = (String) airportSelector.getSelectedItem();

            // Obtener vuelos previstos para el destino seleccionado
            HashMap<Integer, DailyFlight> vuelosPrevistos = vuelosController.getVuelosProximos(destino);

            // Mostrar vuelos previstos en la tabla
            actualizarTablaVuelosPrevistos(vuelosPrevistos);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al obtener los vuelos previstos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEnviar2ActionPerformed

    private void actualizarTablaVuelosPrevistos(HashMap<Integer, DailyFlight> vuelosPrevistos) {
        try {
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.addColumn("Código Vuelo Diario");
            tableModel.addColumn("Fecha Vuelo");
            tableModel.addColumn("Hora Salida");
            tableModel.addColumn("Hora Llegada");

            // Llenar la tabla con los datos de vuelos previstos
            for (DailyFlight dailyFlight : vuelosPrevistos.values()) {
                Object[] rowData = {
                        dailyFlight.getCodigoVueloDiario(),
                        dailyFlight.getFechaVuelo(),
                        dailyFlight.getHoraSalida(),
                        dailyFlight.getHoraLlegada()
                };
                tableModel.addRow(rowData);
            }

            tblLlegadasDatos.setModel(tableModel);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al actualizar la tabla de vuelos previstos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


